import numpy as np
import torch
import torch.nn as nn
import torch.optim as optim
import torchvision
import torchvision.transforms as transforms
from torch.utils.data import DataLoader, Dataset, ConcatDataset
from torch.utils.data import random_split
from PIL import Image
import os

''' Extract mean and std from images '''
# Define transforms for images so mean and std can be calculated
imgdataset_transform = transforms.Compose([
transforms.Resize((224, 224)),
transforms.ToTensor()
])

# Define img_dataset class so images can be batched with transforms
class img_dataset(Dataset):
  def __init__(self, directorypath):
    self.directory = directorypath
    self.filenames = os.listdir(self.directory)
  def __len__(self):
    return len(self.filenames)
  def __getitem__(self, idx): # Corrected: __getitem__
    # No need to sort filenames here; self.filenames already contains them
    target_file = self.filenames[idx]
    full_path = os.path.join(self.directory, target_file) # Corrected: os.path.join and use self.directory
    image = Image.open(full_path).convert("RGB")
    return imgdataset_transform(image)

# Get dataloader object with one batch of size num_items from dataset object labeled 'data'
def get_subset_loader(data, num_items):
  shuffled_indices = torch.randperm(len(data))
  subset_indices = shuffled_indices[:num_items]
  small_dataset = torch.utils.data.Subset(data, subset_indices)
  small_dataloader = torch.utils.data.DataLoader(small_dataset, batch_size=num_items, num_workers=2)
  return small_dataloader

# Get mean and std from dataloader (with single batch)
def get_mean_std(loader):
  batch = next(iter(loader))
  mean = batch.mean(dim=(0,2,3))
  std = batch.std(dim=(0,2,3))
  return mean, std

# Put it all together into a function
def full_image_stats(directorypath):
  dataset = img_dataset(directorypath)
  loader = get_subset_loader(dataset, 3000)
  return get_mean_std(loader)